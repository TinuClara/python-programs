# area and perimeter of circle using proper comments

pi = 3.14                                                #pi is declared
r = int(input( "Enter the radius of the circle : "))     #reading radius
area = pi * r * r                                        #declaring area
perimeter = 2 * pi * r                                   #declaring perimeter
print ("Area of the circle : ",area)                     #value of area is printed
print("Perimeter of the circle is ", perimeter)          #value of perimeter is printed